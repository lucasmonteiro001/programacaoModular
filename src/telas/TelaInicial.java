    private void buttonAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAcessarActionPerformed
        Connection connect;
        char[] pswd = passwordFieldSenha.getPassword();
        usuario = textFieldUsuario.getText();
        senha = String.valueOf(pswd);
        connect = getMySQLConnector(usuario, senha);
        if (connect != null) {
            new TelaOpcoes().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Acesso ao sistema negado.\nTente novamente.");
        }
    }//GEN-LAST:event_buttonAcessarActionPerformed

